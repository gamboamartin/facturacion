    public function obten_registros_relacionables(int $com_cliente_id)
    {
        $query = "
                SELECT
                    fc_factura.id as fc_factura_id,
                    fc_factura.folio as fc_factura_folio,
                    fc_factura.total as fc_factura_total,
                    com_cliente.razon_social as com_cliente_razon_social,
                    fc_factura.fecha as fc_factura_fecha,
                    fc_factura.asignado_fc_layout,
                    fc_factura.monto_por_asignar
                FROM
                    fc_factura
                    LEFT JOIN com_sucursal ON fc_factura.com_sucursal_id = com_sucursal.id
                    LEFT JOIN com_cliente ON com_sucursal.com_cliente_id = com_cliente.id
                    WHERE
                    com_cliente.id = :com_cliente_id 
                    AND fc_factura.etapa = 'TIMBRADO' 
                    AND (fc_factura.asignado_fc_layout = 'inactivo' OR fc_factura.monto_por_asignar > 0)
        ";

        try {
            $stmt = $this->link->prepare($query);
            $stmt->execute([
                ':com_cliente_id' => $com_cliente_id,
            ]);
            $rs = $stmt->fetchAll(\PDO::FETCH_ASSOC);
        } catch (PDOException $e) {
            return (new errores())->error(
                mensaje: $e->getMessage(),
                data:  $e
            );
        }

        return $rs;
    }

    public function obtener_registro_de_relaciones(int $factura_id): array
    {
        $response = [];

        $this->registro_id = $factura_id;
        $rs = $this->obten_data(
            columnas: [
                'fc_factura_folio',
                'fc_factura_total',
                'fc_factura_monto_por_asignar',
                'com_cliente_razon_social'
            ]
        );
        if(errores::$error){
            return (new errores())->error("Error al obtener info de la factura", $rs);
        }

        $response['factura'] = $rs;

        $fc_layout_factura_modelo = new fc_layout_factura(link: $this->link);

        $data = $fc_layout_factura_modelo->filtro_and(
            columnas: [
                'fc_layout_factura_id',
                'fc_layout_factura_monto_relacionado',
                'fc_layout_nom_id',
                'fc_factura_id',
                'fc_layout_nom_codigo',
                'fc_layout_nom_total',
                'com_cliente_razon_social',
                'fc_layout_periodo_descripcion_select'
            ],
            filtro: [
                'fc_layout_factura.fc_factura_id' => $factura_id
            ]
        );

        if(errores::$error){
            return (new errores())->error("Error filtrar en fc_layout_factura", $data);
        }

        $data = $data->registros;

        $relaciones = [];
        foreach ($data as $key => $registro) {
            $link = "index.php?seccion=fc_layout_factura&accion=elimina_relacion";
            $link .= "&registro_id={$registro['fc_layout_factura_id']}";
            $link .= "&session_id={$_GET['session_id']}";
            $link .= "&view=factura&view_id={$registro['fc_factura_id']}";
            $relaciones[$key] = $registro;
            $relaciones[$key]['url_eliminar'] = $link;
        }

        $response['relaciones'] = $relaciones;

        return $response;
    }


